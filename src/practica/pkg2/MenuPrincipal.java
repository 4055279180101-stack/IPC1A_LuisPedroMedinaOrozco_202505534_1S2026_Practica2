    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       //Creamos el objeto donde vamos abir el archivo y le decimos que solo habra archivos
       JFileChooser bw = new JFileChooser();
       bw.setFileSelectionMode(JFileChooser.FILES_ONLY);
       
       //Le decimos que solo va a poder abrir archivos de texto
       FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de texto","txt");
       bw.setFileFilter(filtro);
       
       int res = bw.showOpenDialog(this);
       if(res != JFileChooser.CANCEL_OPTION){
          File name = bw.getSelectedFile();
           
          if((name == null) || name.getName().equals("")){
             JOptionPane.showMessageDialog(this, "Erro al abrir el archivo");
          }else{
            MostrarDatos(name.getAbsolutePath());
          }
       }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void cargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarActionPerformed
       String linea = Dato.getText();
       String[] aux = linea.split(",");
       Practica2.datos = new int[aux.length];
       Practica2.datos_antiguo = new int [aux.length];
       
       //Estadisticas
         comparando.setText("0"); Practica2.Comparaciones=0;
         intercambios.setText("0"); Practica2.Intercambios=0;
         iteraciones.setText("0"); Practica2.Iteraciones=0;
       
         //Mensaje de inicio
         area.setText("");
         area.setText("Arreglo cargado \n \n");
         
       //Verificamos que linea no este vacia
       if(linea.equals(",")||linea==null||linea.equals("")){
         JOptionPane.showMessageDialog(this, "No hay texto agregado");
         return;
       }
       
       //Guardamos los valores en datos
       try {
       for(int i=0; i<aux.length; i++){
          Practica2.datos[i]=Integer.parseInt(aux[i]);
          Practica2.datos_antiguo[i]=Integer.parseInt(aux[i]);
       }
       //Ponemos los colores correspondientes
       Practica2.colores = new Color[Practica2.datos.length];
       for(int i = 0; i < Practica2.colores.length; i++){
        Practica2.colores[i] = Color.BLUE;
       }
       
       PonerGrafica();
       JOptionPane.showMessageDialog(this, "Guardado con exito");
       }catch(NumberFormatException e){
         JOptionPane.showMessageDialog(this, "Tipo de variable incompatible");
       }
    }//GEN-LAST:event_cargarActionPerformed

