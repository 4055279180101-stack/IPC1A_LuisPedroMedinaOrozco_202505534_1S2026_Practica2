public static void PonerGrafica(){
    SwingUtilities.invokeLater(() -> {
        grafica.removeAll();
        
        DefaultCategoryDataset dataset = crearGrafica2(); 
        JFreeChart chart = ChartFactory.createBarChart(null,null,null,dataset);
        
        chart.setBackgroundPaint(Color.black); 
        chart.getPlot().setBackgroundPaint(Color.black); 
        
        BarRenderer renderer = new BarRenderer(){
            @Override
            public Paint getItemPaint(int row, int column){
                return Practica2.colores[column];
            }
        };
        
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setRenderer(renderer);
        
        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(grafica.getSize());
        grafica.setLayout(new BorderLayout());
        grafica.add(chartPanel, BorderLayout.CENTER);
        
        grafica.revalidate();
        grafica.repaint();
    });
}
